/**
 * Делает первую букву строки заглавной, остальные оставляет без изменений.
 *
 * Универсальный хелпер для подписей/лейблов, формируемых из «сырых» строк
 * (имена каталогов, ключи и т. п.). Пустая строка возвращается как есть.
 *
 * @param value Исходная строка.
 * @returns Строка с заглавной первой буквой.
 */
export function capitalize(value: string): string {
  return value.charAt(0).toUpperCase() + value.slice(1);
}
